    <div>
        <h1 class="titulo">¿Qué es la dislexia?</h1>
        <p>La dislexia es una dificultad específica de aprendizaje que forma parte de las dificultades específicas de aprendizaje (DEA), caracterizadas por problemas en procesos básicos como la comprensión oral y escrita del lenguaje. Afecta áreas como pensamiento, habla, lectura, escritura, deletreo y el manejo de signos matemáticos. Aunque no está relacionada con un déficit cognitivo ni sensorial evidente, puede ser causa de bajo rendimiento escolar y dificultades sociales.</p>
        <br>
        <p>Es el trastorno más común en la lectura y aprendizaje, afectando aproximadamente al 2-8% de los niños escolarizados, siendo más prevalente en niños que en niñas y con antecedentes familiares frecuentes. Suele manifestarse junto con trastornos como la discalculia y la disgrafía. Los niños con dislexia pueden también presentar problemas de atención e impulsividad.</p>
        <br>
        <p>El diagnóstico de la dislexia se basa en la observación de un retraso notable en la adquisición de habilidades de lectura en comparación con sus compañeros. Se relaciona con dificultades en el procesamiento fonológico, auditivo y visual, así como con déficits en la memoria de trabajo y en el conocimiento sintáctico.</p>
        <br>
        <p>Además, existen variantes como la disgrafía, que afecta la habilidad para escribir correctamente, y la discalculia, que se refiere a dificultades en el cálculo y el manejo de números. Estos trastornos pueden tener un impacto significativo en el rendimiento académico y emocional de los niños afectados, afectando no solo su desempeño escolar sino también su adaptación social y emocional.</p>
    </div>
</section>

